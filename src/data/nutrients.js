export const NUTRIENT_BY_SLUG = Object.fromEntries(NUTRIENTS.map(n => [n.slug, n]))
export const NUTRIENT_CATEGORIES = ['All', 'Vitamins', 'Minerals', 'Other']

// Match a supplement nutrient label (e.g. "Vitamin D", "Magnesium") to a NUTRIENT entry
export function matchSuppToNutrient(suppLabel) {
  const normalized = suppLabel.toLowerCase()
  return NUTRIENTS.find(n => n.suppMatch.some(term => normalized.includes(term)))
}

// Parse "400 mg" or "2000 IU" into a standard amount for the nutrient's unit
export function parseSuppAmount(valueStr, nutrient) {
  const match = valueStr?.match(/^([\d.]+)\s*(.*)$/)
  if (!match) return 0
  const amount = parseFloat(match[1])
  const unit = match[2].toLowerCase().trim()
  if (unit === 'iu') {
    if (nutrient.suppIuToMcg) {
      // Vitamin D: 1 IU = 0.025 mcg; Vitamin A: 1 IU retinol ≈ 0.3 mcg
      return nutrient.slug === 'vitamin-a' ? amount * 0.3 : amount * 0.025
    }
    return 0
  }
  return amount
}
